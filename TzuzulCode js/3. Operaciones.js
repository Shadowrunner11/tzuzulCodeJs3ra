// Operaciones aritmeticas

// Adicion
var suma = 3 + 5 // -> 8
var suma2 = 3.8 + 7.6 // -> 11.399999999
// Cuando transformamos de decimal flotante a binario 
// siempre obtenemos un resultado infinito
// los flotantes son signo guardan el signo en el bit mas significativo

console.log(suma)
console.log(suma2)

// Multiplicacion
var producto = 4*3 // 12
var producto2 = 2.8*3.2  // 8.96

console.log(producto)
console.log(producto2)

// Division
var resultado = 4/2
var resultado2 = 5/3

console.log(resultado)
console.log(resultado2)

// Modulo
var residuo = 5%3 // -> 2
var residuo2 = 10%4 // -> 2

console.log(residuo)
console.log(residuo2)


// Potenciacion
var potencia1 = 4**2 // -> 16
var potencia2 = 142**2 // -> 20164
var potencia3 = 2.5**2 //  -> 6.25
var potencia4 = 8**3 // -> 512

console.log(potencia1)
console.log(potencia2)
console.log(potencia3)
console.log(potencia4)

